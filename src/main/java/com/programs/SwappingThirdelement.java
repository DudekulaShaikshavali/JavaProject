package com.programs;

public class SwappingThirdelement {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int temp;
        temp = a;
        a = b;
        b = temp;

        System.out.println(" a value : " +a);
        System.out.println(" b value : " +b);
    }
}
